package br.com.k19.padraocriacao.factorymethod;

public class TestaFactoryMethod {

	public static void main(String[] args) {

		/*
		 * Objetivo: Encapsular a escolha da classe concreta a ser utilizada na
		 * criação de objetos de um determinado tipo.
		 * 
		 * Exemplo prático: Considere um sistema bancário que precisa enviar
		 * mensagens aos seus clientes. Por exemplo,após a realização de uma
		 * compra com cartão de crédito, uma mensagem contendo informações sobre
		 * a compra pode ser enviada ao cliente. Se esse cliente for uma pessoa
		 * física, poderá optar pelo recebimento da mensagem através de email ou
		 * SMS. Por outro lado, se for uma pessoa jurídica, poderá também
		 * receber a mensagem através de JMS (Java Message Service).
		 */

		EmissorCreator ec = new EmissorCreator();
		Emissor emissor = ec.create(EmissorEnum.SMS);
		emissor.envia("Conta está utilizando cheque especial");

	}

}
